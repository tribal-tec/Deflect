#include <QGuiApplication>
#include <QCommandLineParser>
#include <deflectQml/QmlStreamer.h>
#include <deflect/version.h>

int main( int argc, char** argv )
{
    QGuiApplication app( argc,argv );
    QGuiApplication::setApplicationVersion(
                QString::fromStdString( deflect::Version::getString( )));
    app.setQuitOnLastWindowClosed( true );

    QCommandLineParser parser;
    parser.setApplicationDescription("QmlStreamer example");
    parser.addHelpOption();
    parser.addVersionOption();

    QCommandLineOption qmlFileOption( "qml", "QML file to load", "qml-file",
                                      "qrc:/qml/gui.qml" );
    parser.addOption( qmlFileOption );

    QCommandLineOption streamNameOption( "name", "Name of the streamer", "name",
                                         "QmlStreamer example" );
    parser.addOption( streamNameOption );

    QCommandLineOption streamHostOption( "host", "Stream hostname", "hostname",
                                         "localhost" );
    parser.addOption( streamHostOption );

    parser.process( app );

    QString qmlFile = parser.value( qmlFileOption );
    QString streamName = parser.value( streamNameOption );
    QString streamHost = parser.value( streamHostOption );

    QScopedPointer< deflect::QmlStreamer > streamer(
                new deflect::QmlStreamer( qmlFile, streamName.toStdString(),
                                          streamHost.toStdString(),
                                          QSize( 1024, 768 )));
    return app.exec();
}
